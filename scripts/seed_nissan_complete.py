def derive(base,trans_type,gears,drive,mass_adj=0,perf_override=None):
    r={}
    for gn,items in base.items():
        new_items=[]
        for k,v in items:
            if k=="Коробка передач": v=trans_type
            elif k=="Количество передач": v=str(gears)
            elif k=="Тип привода": v=drive
            elif k=="Снаряженная масса" and mass_adj:
                try:
                    num=int(re.search(r'(\d+)',v).group(1))
                    v=f"{num+mass_adj} кг"
                except:pass
            new_items.append((k,v))
        r[gn]=new_items
    if perf_override:
        r["Эксплуатационные показатели"]=perf_override
    return r

