def tr(text):
    """Translate RU text to AZ."""
    return KEY_TR.get(text.strip(), text.strip())


def tr_group(text):
    return GROUP_TR.get(text.strip(), text.strip())


def make_specs_json(raw_specs):
    """Convert raw specs dict to our JSON format for DB."""
    result = []
    sort = 0
    for group_name, items in raw_specs.items():
        sort += 1
        translated_items = []
        for key, value in items:
            translated_items.append({
                "key": tr(key),
                "value": tr(value),
            })
        result.append({
            "label": tr_group(group_name),
            "sort_order": sort,
            "items": translated_items,
        })
    return result


def get_db():
    return psycopg2.connect(**DB_CONFIG)


def upsert_specs(gen_slug, specs_json):
    """Insert or update specs for a generation."""
    conn = get_db()
    cur = conn.cursor()

    cur.execute("SELECT id FROM vehicle_generations WHERE slug = %s", (gen_slug,))
    row = cur.fetchone()
    if not row:
        print(f"  SKIP: generation '{gen_slug}' not found")
        cur.close()
        conn.close()
        return False

    gen_id = row[0]

    cur.execute("SELECT id FROM vehicle_generation_specs WHERE vehicle_generation_id = %s", (gen_id,))
    existing = cur.fetchone()

    if existing:
        cur.execute(
            "UPDATE vehicle_generation_specs SET specs = %s, updated_at = NOW() WHERE vehicle_generation_id = %s",
            (json.dumps(specs_json, ensure_ascii=False), gen_id)
        )
    else:
        cur.execute(
            "INSERT INTO vehicle_generation_specs (vehicle_generation_id, specs, created_at, updated_at) VALUES (%s, %s, NOW(), NOW())",
            (gen_id, json.dumps(specs_json, ensure_ascii=False))
        )

    conn.commit()
    cur.close()
    conn.close()
    return True


