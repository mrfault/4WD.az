    row = cur.fetchone()
    if row:
        print(f"  = Brand exists: SsangYong (id={row[0]})")
        return row[0]
    cur.execute("INSERT INTO vehicle_brands (name, slug, is_active, created_at, updated_at) VALUES ('SsangYong', 'ssangyong', true, NOW(), NOW()) RETURNING id")
    bid = cur.fetchone()[0]
