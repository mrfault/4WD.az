def tr(t): return KEY_TR.get(t.strip(), t.strip())
def tr_g(t): return GROUP_TR.get(t.strip(), t.strip())

def make_groups(raw):
    r, s = [], 0
    for gn, items in raw.items():
        s += 1
        r.append({"label": tr_g(gn), "sort_order": s, "items": [{"key": tr(k), "value": tr(v)} for k, v in items]})
    return r

def slugify(t):
    t = t.lower().strip()
    t = re.sub(r"[^a-z0-9\s-]", "", t)
    return re.sub(r"[\s-]+", "-", t)[:80].strip("-")


