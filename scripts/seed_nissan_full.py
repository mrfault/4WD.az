def ensure_models(cur):
    for slug, name in MODELS.items():
        cur.execute("SELECT id FROM vehicle_models WHERE vehicle_brand_id=%s AND slug=%s", (NISSAN_BRAND_ID, slug))
        if not cur.fetchone():
            cur.execute("INSERT INTO vehicle_models (vehicle_brand_id,name,slug,is_active,created_at,updated_at) VALUES(%s,%s,%s,true,NOW(),NOW()) RETURNING id", (NISSAN_BRAND_ID, name, slug))
            print(f"  + Model: {name} (id={cur.fetchone()[0]})")
        else:
            print(f"  = Model: {name}")

def seed_all(cur):
    gen_count = 0
    spec_count = 0
    for model_slug, generations in DATA.items():
        cur.execute("SELECT id FROM vehicle_models WHERE vehicle_brand_id=%s AND slug=%s", (NISSAN_BRAND_ID, model_slug))
        row = cur.fetchone()
        if not row:
            print(f"  SKIP: model {model_slug} not found")
            continue
        model_id = row[0]

        for sort_idx, (gen_name, gen_slug, y_from, y_to, mods) in enumerate(generations):
            # Upsert generation
            cur.execute("SELECT id FROM vehicle_generations WHERE slug=%s", (gen_slug,))
            existing = cur.fetchone()
            if existing:
                gen_id = existing[0]
                cur.execute("UPDATE vehicle_generations SET name=%s,year_from=%s,year_to=%s,sort_order=%s,is_active=true,updated_at=NOW() WHERE id=%s",
                    (gen_name, y_from, y_to, sort_idx, gen_id))
            else:
                cur.execute("INSERT INTO vehicle_generations (vehicle_model_id,name,slug,year_from,year_to,is_active,sort_order,created_at,updated_at) VALUES(%s,%s,%s,%s,%s,true,%s,NOW(),NOW()) RETURNING id",
                    (model_id, gen_name, gen_slug, y_from, y_to, sort_idx))
                gen_id = cur.fetchone()[0]
                gen_count += 1

            # Build specs JSON
            if len(mods) == 1:
                specs_json = make_groups(mods[0][1])
            else:
                specs_json = {"modifications": [{"name": n, "groups": make_groups(s)} for n, s in mods]}

            # Upsert specs
            json_str = json.dumps(specs_json, ensure_ascii=False)
            cur.execute("SELECT id FROM vehicle_generation_specs WHERE vehicle_generation_id=%s", (gen_id,))
            if cur.fetchone():
                cur.execute("UPDATE vehicle_generation_specs SET specs=%s,updated_at=NOW() WHERE vehicle_generation_id=%s", (json_str, gen_id))
            else:
                cur.execute("INSERT INTO vehicle_generation_specs (vehicle_generation_id,specs,created_at,updated_at) VALUES(%s,%s,NOW(),NOW())", (gen_id, json_str))
            spec_count += 1
            mod_count = len(mods)
            print(f"  {'+'if not existing else '~'} {gen_slug} ({mod_count} mod{'s'if mod_count>1 else ''})")

    return gen_count, spec_count

def main():
    print("=" * 60)
    print("4WD.az — Nissan FULL Catalog Seeder v2")
    print("=" * 60)
    conn = get_db()
    cur = conn.cursor()

    print("\n[1] Ensuring models...")
    ensure_models(cur)

    print("\n[2] Seeding generations + specs...")
    gc, sc = seed_all(cur)

    conn.commit()
    cur.close()
    conn.close()
    print(f"\n{'='*60}")
    print(f"Done! {gc} new generations, {sc} specs upserted")
    print("="*60)

if __name__ == "__main__":
    main()
