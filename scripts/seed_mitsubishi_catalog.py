    conn = get_db()
    cur = conn.cursor()

    print("\n[1] Ensuring brand...")
    brand_id = ensure_brand(cur)

    print("\n[2] Ensuring models...")
    ensure_models(cur, brand_id)

    print("\n[3] Deactivating old duplicate models...")
    old_slugs = ["pajero-iii", "pajero-iv", "l200-triton", "montero"]
