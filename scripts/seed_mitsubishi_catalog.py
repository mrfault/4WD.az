def ensure_brand(cur):
    """Ensure Mitsubishi brand exists, return its ID."""
    cur.execute("SELECT id FROM vehicle_brands WHERE slug = 'mitsubishi'")
    row = cur.fetchone()
    if row:
        print(f"  = Brand exists: Mitsubishi (id={row[0]})")
        return row[0]
    cur.execute("""
        INSERT INTO vehicle_brands (name, slug, is_active, created_at, updated_at)
        VALUES ('Mitsubishi', 'mitsubishi', true, NOW(), NOW()) RETURNING id
    """)
    bid = cur.fetchone()[0]
    print(f"  + Brand: Mitsubishi (id={bid})")
    return bid


def ensure_models(cur, brand_id):
    """Ensure all required models exist."""
    for slug, name in REQUIRED_MODELS.items():
        cur.execute(
            "SELECT id FROM vehicle_models WHERE vehicle_brand_id = %s AND slug = %s",
            (brand_id, slug)
        )
        row = cur.fetchone()
        if not row:
            cur.execute("""
                INSERT INTO vehicle_models (vehicle_brand_id, name, slug, year_from, is_active, created_at, updated_at)
                VALUES (%s, %s, %s, 0, true, NOW(), NOW()) RETURNING id
            """, (brand_id, name, slug))
            mid = cur.fetchone()[0]
            print(f"  + Model: {name} (id={mid})")
        else:
            print(f"  = Model exists: {name} (id={row[0]})")


def seed_generations(cur, brand_id):
    """Insert or update generations."""
    inserted = 0
    for model_slug, gens in GENERATIONS.items():
        cur.execute(
            "SELECT id FROM vehicle_models WHERE vehicle_brand_id = %s AND slug = %s",
            (brand_id, model_slug)
        )
        model_row = cur.fetchone()
        if not model_row:
            print(f"  SKIP model: {model_slug} (not found)")
            continue
        model_id = model_row[0]

        for sort_idx, (gen_name, gen_slug, year_from, year_to) in enumerate(gens):
            cur.execute("SELECT id FROM vehicle_generations WHERE slug = %s", (gen_slug,))
            existing = cur.fetchone()
            if existing:
                cur.execute("""
                    UPDATE vehicle_generations
                    SET name = %s, year_from = %s, year_to = %s, sort_order = %s, updated_at = NOW()
                    WHERE slug = %s
                """, (gen_name, year_from, year_to, sort_idx, gen_slug))
                print(f"  ~ Updated: {gen_slug}")
            else:
                cur.execute("""
                    INSERT INTO vehicle_generations
                        (vehicle_model_id, name, slug, year_from, year_to, is_active, sort_order, created_at, updated_at)
                    VALUES (%s, %s, %s, %s, %s, true, %s, NOW(), NOW())
                    RETURNING id
                """, (model_id, gen_name, gen_slug, year_from, year_to, sort_idx))
                gid = cur.fetchone()[0]
                print(f"  + Generation: {gen_slug} (id={gid})")
                inserted += 1
    return inserted


def seed_specs(cur):
    """Insert or update specs for generations."""
    ok = 0
    for gen_slug, modifications in SPECS_DATA.items():
        cur.execute("SELECT id FROM vehicle_generations WHERE slug = %s", (gen_slug,))
        row = cur.fetchone()
        if not row:
            print(f"  SKIP specs: {gen_slug} (gen not found)")
            continue
        gen_id = row[0]

        # Build specs JSON
        if len(modifications) == 1:
            _, raw_specs = modifications[0]
            specs_json = make_specs(raw_specs)
        else:
            specs_json = make_modification_specs(modifications)

        # Upsert
        cur.execute(
            "SELECT id FROM vehicle_generation_specs WHERE vehicle_generation_id = %s",
            (gen_id,)
        )
        existing = cur.fetchone()
        json_str = json.dumps(specs_json, ensure_ascii=False)

        if existing:
            cur.execute(
                "UPDATE vehicle_generation_specs SET specs = %s, updated_at = NOW() WHERE vehicle_generation_id = %s",
                (json_str, gen_id)
            )
        else:
            cur.execute(
                "INSERT INTO vehicle_generation_specs (vehicle_generation_id, specs, created_at, updated_at) VALUES (%s, %s, NOW(), NOW())",
                (gen_id, json_str)
            )
        print(f"  + Specs: {gen_slug} ({len(modifications)} mod(s))")
        ok += 1
    return ok


