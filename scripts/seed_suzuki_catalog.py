def tr(text):
    t = text.strip()
    return KEY_TR.get(t, t)


def tr_group(text):
    return GROUP_TR.get(text.strip(), text.strip())


_UNIT_REPLACEMENTS = [
    ("л/100км", "l/100km"), ("л/100 км", "l/100km"), ("км/ч", "km/saat"),
    ("об/мин", "d/d"), ("л.с.", "a.g."), ("см³", "sm³"), ("мм", "mm"),
    ("кг", "kq"), ("Нм", "Nm"), ("Н⋅м", "Nm"), ("кВт", "kVt"), ("г/км", "q/km"),
    ("АИ-", "AI-"), ("Евро ", "Avro "), (" при ", " @ "), (" л", " l"),
]


def tr_val(text):
    t = tr(text)
    for ru, az in _UNIT_REPLACEMENTS:
        t = t.replace(ru, az)
    return t


def make_specs(raw):
    """Convert raw specs dict -> translated JSON array."""
    result = []
    sort = 0
    for group_name, items in raw.items():
        sort += 1
        translated = []
        for key, val in items:
            translated.append({"key": tr(key), "value": tr_val(val)})
        result.append({
            "label": tr_group(group_name),
            "sort_order": sort,
            "items": translated,
        })
    return result


def make_modification_specs(modifications):
    """Convert list of (name, raw_specs) -> modifications JSON."""
    mods = []
    for name, raw in modifications:
        mods.append({
            "name": name,
            "groups": make_specs(raw),
        })
    return {"modifications": mods}


def slugify(text):
    text = text.lower().strip()
    text = re.sub(r"[^a-z0-9\s-]", "", text)
    text = re.sub(r"[\s-]+", "-", text)
    return text[:80].strip("-")


